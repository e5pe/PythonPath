""" 
== -> Value Equality
!= -> Not equal to
< -> Less than
> -> Greater than 
<= -> Less than or equal to
>= -> Greater than or equal to
"""
bool_one = 10 == 10
print(bool_one)

bool_two = 10 == 11
print(bool_two)

not_equal = 10 != 10
print(not_equal)

lessThan = 10 < 20
print(lessThan)

greaterThan = 10 >= 20
print(greaterThan)